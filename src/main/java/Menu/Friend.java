package Menu;

import java.io.Serializable;

/**
 *
 * @author ACER
 */
public class Friend implements Serializable {

    public Friend() {
    }

    public Friend(String name, String ip) {
        this.name = name;
        this.ip = ip;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }
    public String name;
    public String ip;
}
